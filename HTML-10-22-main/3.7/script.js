// Завдання з селекторами
$(document).ready(function () {
  // 1.1 Вибрати всі елементи <p> та змінити їх текст на "Змінений текст"
  $('div#task1 p').text('Змінений текст');

  // 1.2 Вибрати елемент з ідентифікатором task1 та додати йому клас highlight
  $('#task1').addClass('highlight');

  // 1.3 Вибрати всі елементи <li> та додати їм клас list-item
  $('#task2 li').addClass('list-item');

  // 2. Завдання зі змінами стилів
  // 2.1 Змінити колір тексту всіх елементів з класом highlight
  $('.highlight').css('color', 'blue');

  // 2.2 Змінити фон всіх елементів з класом list-item на червоний
  $('.list-item').css('background-color', 'red');

  // 3. Завдання з анімацією
  // 3.1 Зробити анімаційний ефект для елементів з класом highlight (наприклад, fadeIn або slideDown)
  $('.highlight').slideDown(1000);

  // 4. Завдання на зміну вмісту
  // 4.1 Змінити текст останнього елемента <p> у блоку з ідентифікатором task1 на "Новий останній текст"
  $('#task1 p:last-child').text('Новий останній текст');

  // 5. Додаткове завдання
  // 5.1 Додати обробник подій для кнопки з id="removeItemsBtn"
  $('#removeItemsBtn').on('click', function () {
    // 5.2 Видалити всі елементи з класом list-item
    $('.list-item').remove();
  });
});
